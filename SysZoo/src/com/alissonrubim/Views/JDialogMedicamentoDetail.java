    private Medicamento  current = null;
    private void loadDetail(Medicamento a){
        current = a;
        
        jTextFieldNome.setText(a.getNome());
    }

    private void onConfirm(){
        String nome = jTextFieldNome.getText();
        if(nome.isEmpty()){
            JOptionPane.showMessageDialog(this, "O campo nome é de preenchimento obrigatório", "Atenção", JOptionPane.OK_OPTION);
            return;
        }
    
        if(this.current == null){
            Medicamento e = new Medicamento(-1, nome);
            try {
                new MedicamentoDAO().insert(e);
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Ocorreu um erro ao inserir um novo medicamento: " + ex.getMessage(), "Atenção", JOptionPane.OK_OPTION);
            }
        }else{
            this.current.setNome(nome);            
            try {
                new MedicamentoDAO().update(this.current);
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Ocorreu um erro ao atualizar o medicamento", "Atenção", JOptionPane.OK_OPTION);
            }
        }
        setVisible(false);
        
    }
    
    private void onClose(){
       setVisible(false); 
    }
